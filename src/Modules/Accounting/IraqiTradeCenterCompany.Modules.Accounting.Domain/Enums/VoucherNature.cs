namespace IraqiTradeCenterCompany.Modules.Accounting.Domain.Enums;

/// <summary>
/// طبيعة السند: تحدّد أي طرف يكون مديناً وأي طرف يكون دائناً عند فتح السند المستقل.
/// - Debit: السند يُسجّل قيداً يكون فيه "حساب الصندوق/الحساب المختار" مدينًا، والطرف الآخر دائناً.
///   أمثلة: سند قبض، قيد افتتاحي بضاعة وارد.
/// - Credit: السند يُسجّل قيداً يكون فيه "حساب الصندوق/الحساب المختار" دائناً، والطرف الآخر مديناً.
///   أمثلة: سند دفع، قيد إقفال.
/// - Mixed: لا توجد طبيعة محدّدة (مناسب للقيد العام/قيد التسوية)؛ المستخدم يُدخل القيد كاملاً.
/// </summary>
public enum VoucherNature
{
    Mixed = 0,
    Debit = 1,
    Credit = 2,
}
